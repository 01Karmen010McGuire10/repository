                        The preliminary program &lt;http://dc2006.ucol.mx/program.htm&gt; for DC-2006, to be held in Manzanillo, Colima, Mexico on 3-7 October 2006, is now available on the conference's web site &lt;<a href="http://dc2006.ucol.mx">http://dc2006.ucol.mx</a>/&gt;. The conference features two keynote presentations, one on 3 October given by Abel Packer of Brazil's BIREME and one on 6 October given by Michael Crandall of the University of Washington's Information School, during three plenary sessions and four parallel paper sessions, 21 long and 7 short papers will be presented. In addition, seven papers will be read during a special session on Portuguese and Spanish Latin American projects.<br />
